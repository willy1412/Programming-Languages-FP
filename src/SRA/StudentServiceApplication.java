    public void showMajorList(){
        dlm = new DefaultListModel();
        
        for(int i=0 ; i<courseMajor.size() ; i++){
            dlm.addElement(courseMajor.get(i).getCourseMajor());
        }
        majorList.setModel(dlm);
    }
    
