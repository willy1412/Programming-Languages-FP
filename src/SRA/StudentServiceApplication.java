    private void classListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_classListMouseClicked
        // TODO add your handling code here:
        for(int i=0 ; i<enrollClass.size() ; i++){
            if(enrollClass.get(i).getCourseCode().equals(classList.getSelectedValue())){
                classNameField.setText(enrollClass.get(i).getCourseName());
                classQuotaField.setText(enrollClass.get(i).getCourseQuota());
            }
        }
    }//GEN-LAST:event_classListMouseClicked

